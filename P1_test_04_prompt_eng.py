# 섹션 4: 쿼리 분해 (Query Decomposition)
# (교재 05장 기준)
#
# 복잡한 요청 하나를 그대로 던지면 모델이 일부를 빠뜨립니다.
# 큰 질문을 독립적인 하위 질문으로 나눠 각각 답하게 하면
# 정확도와 일관성이 올라갑니다.
#
# 코드 분해: 각 단계를 우리가 통제 → 정형화된 작업에 적합
# 모델 분해: 빠르고 간단하지만 단계가 매번 달라질 수 있음
# ============================================================
print("\n" + "=" * 60)
print("섹션 4: 쿼리 분해 (Query Decomposition)")
print("=" * 60)

# 공통 맥락: 요금제 정보
context = """요금제 정보:
- 스탠다드: 월 5.5만원, 데이터 110GB
- 프리미엄: 월 8.9만원, 데이터 무제한
참고: 영상 1시간 ≈ 1GB, 대학생은 보통 하루 2~3시간 영상 시청."""

# 코드로 분해: 하위 질문 리스트를 순서대로 호출하고 결과를 모음
subqs = [
    "스탠다드(110GB)로 하루 2~3시간 영상 시청 시 한 달 데이터가 충분한가? 계산 근거와 함께.",
    "영상을 많이 보는 대학생에게 스탠다드와 프리미엄 중 무엇을 추천하나? 한 줄 이유.",
]
answers = []
print("\n[코드 분해 - 하위 질문 순차 호출]")
for i, q in enumerate(subqs, 1):
    a = ask(f"{context}\n\n질문: {q}", max_tokens=250)
    print(f"[Q{i}] {q}")
    print(f"[A{i}] {a}\n")
    answers.append(a)

# 하위 답을 모아 최종 종합
synthesis = f"""다음 분석 결과를 바탕으로 최종 추천을 표 1개로 정리해줘.
컬럼: 요금제 | 월요금 | 영상시청 적합성 | 추천여부

분석:
{answers[0]}
{answers[1]}"""
print("[최종 종합]")
print(ask(synthesis, max_tokens=400))

# 모델에게 분해를 맡기기: 직접 하위 질문을 짜는 대신 모델에게 위임
auto = f"""{context}
요청: 영상 많이 보는 대학생에게 이 요금제가 맞는지 판단하고 대안도 추천해줘.
먼저 이 요청을 답하기 위해 필요한 하위 질문 2~3개를 나열하고,
각 질문에 답한 뒤, 마지막에 '최종 추천:' 한 줄로 정리해줘."""
print("\n[모델 분해 - 모델이 스스로 질문 나눔]")
print(ask(auto, max_tokens=600))


