print("\n[CoT 없이]")
print(ask(no_cot, max_tokens=50))

# CoT 적용: 단계적으로 생각한 뒤 마지막 줄에 답
cot = f"""{MATH_Q}
단계적으로 계산한 뒤, 마지막 줄에 '정답: N' 형식으로 적어줘."""
print("\n[CoT 적용 - 풀이 과정 확인]")
print(ask(cot, max_tokens=300))

# 분류에도 CoT: 근거 먼저, 결론 나중 → 오답 줄임
