# 섹션 6: 출력 형식 강제 — JSON 받기
# (교재 08장 기준)
#
# 결과를 코드로 바로 파싱하려면 JSON이 깔끔하게 나와야 합니다.
# 세 가지로 거의 해결됩니다:
#   1) 스키마 명시: 키 이름·타입을 프롬프트에 적기
#   2) 예시 하나 주기: 원하는 JSON 모양을 그대로 보여주기
#   3) "JSON만" 못박기: "설명·코드펜스 없이 JSON 객체만 출력"
# ============================================================
print("\n" + "=" * 60)
print("섹션 6: 출력 형식 강제 — JSON 받기")
print("=" * 60)

def extract(review: str) -> dict:
    """후기에서 정보를 추출해 dict로 반환하는 함수"""
    prompt = f"""다음 후기에서 정보를 뽑아 JSON으로만 출력해줘.
설명이나 코드펜스(```) 없이 JSON 객체 하나만.

키와 타입:
- summary (문자열): 한 줄 요약
- rating (정수 1~5): 별점
- sentiment (문자열): "긍정" | "부정" | "중립"

예시 출력:
{{"summary": "...", "rating": 4, "sentiment": "긍정"}}

후기: {review}"""
    text = ask(prompt, max_tokens=300).strip()
    # 코드펜스(```json ... ```)가 섞여도 안전하게 제거
    text = re.sub(r"^```(?:json)?\s*|\s*```$", "", text).strip()
    return json.loads(text)

reviews = [
    "배송은 빨랐지만 포장이 부실했어요. 제품은 만족합니다.",
    "최고예요 또 살래요",
    "별로... 환불했어요",
]
print()
for r in reviews:
    try:
        d = extract(r)
        print(f"후기: {r}")
        print(f"  → {d['rating']}점 / {d['sentiment']} / {d['summary']}\n")
    except json.JSONDecodeError as e:
        print(f"파싱 실패 (재시도 필요): {e}")


