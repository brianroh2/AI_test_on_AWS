# 섹션 8: 자기 점검 (self-critique)
# (교재 10장 기준)
#
# 1단계: 초안 답을 받는다
# 2단계: "방금 답을 검토하고 틀린 곳을 고쳐라"고 다시 시킨다
#
# 사람의 "초고 → 퇴고"와 같습니다.
# 비용은 호출 1회만큼 늘지만, 사실 오류·형식 누락을 잡는데 효과적.
# 다만 모든 작업에 쓰진 마세요 — 정확도가 중요한 작업에만 적용.
# ============================================================
print("\n" + "=" * 60)
print("섹션 8: 자기 점검 (self-critique)")
print("=" * 60)

# 초안 받고 스스로 점검
question = "다음 일정의 총 소요시간을 분 단위로 계산해줘: 회의 1시간 30분, 이동 45분, 점심 1시간."

draft = ask(question + " 답만 간단히.", max_tokens=150)
print(f"\n[초안]\n{draft}")

review = f"""아래 질문과 초안 답을 검토해줘.
계산이 맞는지 단계별로 확인하고, 틀렸으면 고쳐서 마지막 줄에 '최종: N분'으로 적어줘.

질문: {question}
초안 답: {draft}"""
print(f"\n[검토 후]\n{ask(review, max_tokens=300)}")

# 점검 항목을 정해주기: 체크리스트를 주면 검토가 더 일관됨
text = "당사는 2010년 설립되어 현재 직원 50명, 작년 매출 30억을 달성한 업게 1위 기업입니다."
critique = f"""다음 홍보 문구를 검토해줘. 점검 항목:
1) 오타·맞춤법
2) 과장·근거 없는 표현
3) 더 자연스러운 문장 제안

문구: {text}

각 항목별로 지적하고, 마지막에 '수정본:'으로 고친 문장을 줘."""
print(f"\n[항목별 자기 점검]\n{ask(critique, max_tokens=400)}")


