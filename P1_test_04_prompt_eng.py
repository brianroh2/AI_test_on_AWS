# 섹션 3: Chain-of-Thought (CoT) — 생각의 사슬
# (교재 04장 기준)
#
# CoT는 모델에게 답을 바로 내지 말고 풀이 과정을 적게 하는 기법.
# 사람도 암산보다 풀어 쓰면 덜 틀리듯,
# 모델도 중간 단계를 적으면 다단계 추론에서 정확도가 올라갑니다.
#
# 주의: CoT는 출력 토큰이 늘어 비용·지연이 커집니다.
#       단순 작업엔 쓰지 말고, 다단계 추론·계산·미묘한 판단에만 쓰세요.
# ============================================================
print("\n" + "=" * 60)
print("섹션 3: Chain-of-Thought (CoT)")
print("=" * 60)

MATH_Q = "한 상자에 사과가 24개 있다. 3상자를 사서 5명이 똑같이 나누면 한 사람당 몇 개?"

# CoT 없이: 바로 답 요구 → 다단계 문제에서 실수 잦음
