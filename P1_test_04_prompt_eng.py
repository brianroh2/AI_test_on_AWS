import boto3
import json
import re

# ── 공통 설정 ────────────────────────────────────────────────
REGION = "us-east-1"
MODEL_ID = "us.anthropic.claude-sonnet-4-6"  # 교재 기본 모델

# bedrock-runtime 클라이언트 생성
bedrock = boto3.client("bedrock-runtime", region_name=REGION)


# ── 공통 헬퍼 함수 ───────────────────────────────────────────
# 교재 01-3. 공통 호출 헬퍼와 동일한 구조
# 시스템 프롬프트(system)와 사용자 메시지를 받아 답변 텍스트를 반환
def ask(user_text: str, system: str = None, max_tokens: int = 800) -> str:
    """Converse API로 한 번 호출하고 답변 텍스트를 반환한다."""
    kwargs = {
        "modelId": MODEL_ID,
        "messages": [{"role": "user", "content": [{"text": user_text}]}],
        "inferenceConfig": {"maxTokens": max_tokens},
    }
    if system:
        kwargs["system"] = [{"text": system}]
    resp = bedrock.converse(**kwargs)
    return resp["output"]["message"]["content"][0]["text"]


